int dh_init_group14(DH_CTX *ctx) {
    bn_from_bytes_be(&ctx->p, GROUP14_P, sizeof(GROUP14_P));
    bn_from_bytes_be(&ctx->g, GROUP14_G, sizeof(GROUP14_G));
    bn_zero(&ctx->private_key);
    bn_zero(&ctx->public_key);
    return 0;
}

void dh_generate_keys(DH_CTX *ctx, CSPRNG_CTX *rng) {
    /* Generate a 2048-bit private key in [2, p-2] */
    uint8_t priv_bytes[DH_KEY_BYTES];

    /* Fill with CSPRNG output */
    for (int i = 0; i < DH_KEY_BYTES; i += 4) {
        uint32_t w = csprng_random32(rng);
        priv_bytes[i+0] = (uint8_t)(w >> 24);
        priv_bytes[i+1] = (uint8_t)(w >> 16);
        priv_bytes[i+2] = (uint8_t)(w >>  8);
        priv_bytes[i+3] = (uint8_t)(w      );
    }
    /* Force it into [2, p-2]: clear top byte jitter, set bit 1 */
    priv_bytes[0] = 0x7F;   /* keep well below p */
    priv_bytes[DH_KEY_BYTES-1] |= 0x02; /* ensure >= 2 */

    bn_from_bytes_be(&ctx->private_key, priv_bytes, DH_KEY_BYTES);

    /* public = g^private mod p */
    bn_modpow(&ctx->public_key, &ctx->g, &ctx->private_key, &ctx->p);
}

void dh_export_public(const DH_CTX *ctx, uint8_t *out, size_t len) {
    bn_to_bytes_be(&ctx->public_key, out, len);
}

int dh_shared_secret(DH_CTX *ctx,
                     const uint8_t *peer_public, size_t peer_len,
                     uint8_t *out_secret)
{
    BigNum peer;
    BigNum one, p_minus_1;
    BigNum secret;

    bn_from_bytes_be(&peer, peer_public, peer_len);

    /* Validate: peer must be in [2, p-2] */
    bn_zero(&one); one.d[0] = 1;

    /* peer <= 1 → invalid */
    if (bn_cmp(&peer, &one) <= 0) return -1;

    /* peer >= p-1 → invalid */
    bn_copy(&p_minus_1, &ctx->p);
    bn_sub(&p_minus_1, &p_minus_1, &one);
    if (bn_cmp(&peer, &p_minus_1) >= 0) return -1;

    /* shared = peer^private mod p */
    bn_modpow(&secret, &peer, &ctx->private_key, &ctx->p);
    bn_to_bytes_be(&secret, out_secret, DH_KEY_BYTES);

    /* Scrub secret from stack */
    memset(secret.d, 0, sizeof(secret.d));
    return 0;
}